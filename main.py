# Create admin_only decorator function
def admin_only(func):
    @wraps(func)
    def wrapper(*args, **kwargs):
        # If id is not 1 then return abort with 403 error
        if current_user.id != 1:
            abort(403)  # Raise a 403 Forbidden error
        return func(*args, **kwargs)  # Otherwise continue with the route function

    return wrapper


